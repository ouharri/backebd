package com.ouharri.Jobinow.model.enums;

/**
 * Enum representing the status of a company.
 */
public enum CompanyStatus {
    /**
     * The company has been approved.
     */
    APPROVED,

    /**
     * The company is pending approval.
     */
    PENDING,

    /**
     * The company has been rejected.
     */
    REJECTED,

    /**
     * The company is blacklisted.
     */
    BLACKLISTED
}
